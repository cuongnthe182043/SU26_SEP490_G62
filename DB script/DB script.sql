

DROP SCHEMA IF EXISTS public CASCADE;
CREATE SCHEMA public;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE roles (
    id   INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    name TEXT UNIQUE NOT NULL  -- manager | coordinator | accountant | driver
);

INSERT INTO roles (name) VALUES
    ('manager'), ('coordinator'), ('accountant'), ('driver')
ON CONFLICT (name) DO NOTHING;

CREATE TABLE accounts (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    email           TEXT UNIQUE NOT NULL,
    password_hash   TEXT NOT NULL,
    role_id         INT NOT NULL REFERENCES roles(id),
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    last_login_at   TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE profiles (
    id          INT PRIMARY KEY REFERENCES accounts(id) ON DELETE CASCADE,
    full_name   TEXT NOT NULL,
    phone       TEXT UNIQUE,
    role_id     INT NOT NULL REFERENCES roles(id),
    avatar_url  TEXT,
    dob         DATE,
    gender      TEXT CHECK (gender IN ('male','female','other')),
    address     TEXT,
    city        TEXT,
    country     TEXT NOT NULL DEFAULT 'VN',
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);


CREATE TABLE vehicle_groups (
    id                  INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    name                TEXT NOT NULL UNIQUE,           -- "5m2" | "1T25" | "2T5" (spec §26)
    description         TEXT,
    max_load_weight_kg  NUMERIC(10,2),
    price_per_km        NUMERIC(12,2) NOT NULL,
    depreciation_per_km NUMERIC(10,2) NOT NULL DEFAULT 0,
    upgrade_allowed     BOOLEAN NOT NULL DEFAULT FALSE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE vehicles (
    id                  INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    plate_number        TEXT UNIQUE NOT NULL,
    vehicle_group_id    INT NOT NULL REFERENCES vehicle_groups(id),
    brand               TEXT,
    model               TEXT,
    load_capacity_kg    NUMERIC(10,2),
    manufacture_year    SMALLINT,
    purchase_date       DATE,
    assigned_driver_id  INT REFERENCES profiles(id),   -- 1-1, enforced via drivers.vehicle_id UNIQUE
    status              TEXT NOT NULL DEFAULT 'active'
                            CHECK (status IN ('active','retired','maintenance','broken')),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE drivers (
    profile_id              INT PRIMARY KEY REFERENCES profiles(id) ON DELETE CASCADE,
    vehicle_id              INT UNIQUE REFERENCES vehicles(id),   -- UNIQUE → 1-1 (spec §9)
    license_number          TEXT NOT NULL,
    license_expiry_date     DATE,
    hire_date               DATE NOT NULL,    -- <12 months → 8M base; >=12 → 9M (spec §28)
    emergency_contact_name  TEXT,
    emergency_contact_phone TEXT,
    revenue_share_percent   NUMERIC(5,2) NOT NULL DEFAULT 15.00,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW()
);



CREATE TABLE customers (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    customer_type   TEXT NOT NULL CHECK (customer_type IN ('individual','business')),
    full_name       TEXT,
    company_name    TEXT,
    contact_person  TEXT,
    phone           TEXT NOT NULL,
    email           TEXT,
    address         TEXT,
    tax_code        TEXT,
    current_debt    NUMERIC(12,2) NOT NULL DEFAULT 0,
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE partners (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    company_name    TEXT NOT NULL,
    contact_person  TEXT,
    phone           TEXT,
    email           TEXT,
    address         TEXT,
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);


CREATE TABLE orders (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    customer_id     INT REFERENCES customers(id),
    created_by      INT NOT NULL REFERENCES profiles(id),
    updated_by      INT REFERENCES profiles(id),

    cargo_name          TEXT,
    cargo_weight_kg     NUMERIC(10,2),
    payment_type        TEXT CHECK (payment_type IN ('cash','bank_transfer','client_credit')),

    total_estimated_price   NUMERIC(12,2) NOT NULL DEFAULT 0,
    total_actual_price      NUMERIC(12,2) NOT NULL DEFAULT 0,
    derived_status      TEXT NOT NULL DEFAULT 'open'
                            CHECK (derived_status IN ('open','completed','cancelled','partial')),
    is_confidential     BOOLEAN NOT NULL DEFAULT FALSE,
    notes               TEXT,
    import_batch_id     TEXT,    
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 5 — ORDER SHIPMENTS  (= "Trip" in spec)
-- Primary operational unit. Coordinator and Driver work here. (spec §5, §30)
-- =============================================================================

CREATE TABLE order_shipments (
    id                  INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    order_id            INT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    shipment_index      SMALLINT NOT NULL,       -- 1-based within order
    trip_code           TEXT UNIQUE,             -- human-readable: "CH-2024-00123"
    vehicle_group_id    INT NOT NULL REFERENCES vehicle_groups(id),
    owner_driver_id     INT REFERENCES profiles(id),
    vehicle_id          INT REFERENCES vehicles(id),
    estimated_price     NUMERIC(12,2),
    estimated_distance_km NUMERIC(10,2),
    actual_distance_km NUMERIC(10,2),
    actual_price        NUMERIC(12,2),
    cargo_name          TEXT,
    cargo_weight_kg     NUMERIC(10,2),
    status              TEXT NOT NULL DEFAULT 'available'
                            CHECK (status IN (
                                'available',  
                                'claimed', 
                                'picking',   
                                'loaded',  
                                'transit', 
                                'arrived',   
                                'completed',   
                                'failed',   
                                'returning',  
                                'cancelled'  
                            )),

    version             INT NOT NULL DEFAULT 0,

    notes               TEXT,
    cancel_reason       TEXT,

    claimed_at          TIMESTAMPTZ,
    picking_at          TIMESTAMPTZ,
    loaded_at           TIMESTAMPTZ,
    transit_at          TIMESTAMPTZ,
    arrived_at          TIMESTAMPTZ,
    completed_at        TIMESTAMPTZ,
    failed_at           TIMESTAMPTZ,
    returning_at        TIMESTAMPTZ,   -- ← added: trả hàng về
    cancelled_at        TIMESTAMPTZ,

    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    UNIQUE (order_id, shipment_index)
);



CREATE TABLE trip_stops (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    shipment_id     INT NOT NULL REFERENCES order_shipments(id) ON DELETE CASCADE,
    stop_index      SMALLINT NOT NULL,   -- 1-based sequence order
    stop_type       TEXT NOT NULL CHECK (stop_type IN ('pickup','delivery')),
    address         TEXT NOT NULL,
    contact_name    TEXT,
    contact_phone   TEXT,
    notes           TEXT,
    arrived_at      TIMESTAMPTZ,         -- driver confirmed arrival
    completed_at    TIMESTAMPTZ,         -- driver completed this stop
    proof_url       TEXT,                -- optional per-stop photo evidence
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    UNIQUE (shipment_id, stop_index)
);



CREATE TABLE trip_runs (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    shipment_id     INT NOT NULL REFERENCES order_shipments(id) ON DELETE CASCADE,
