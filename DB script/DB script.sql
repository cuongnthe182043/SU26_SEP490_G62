CREATE TABLE bonus_records (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    driver_id       INT NOT NULL REFERENCES profiles(id),
    bonus_rule_id   INT REFERENCES bonus_rules(id),
    amount          NUMERIC(12,2) NOT NULL,
    awarded_month   SMALLINT CHECK (awarded_month BETWEEN 1 AND 12),
    awarded_year    SMALLINT,
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
