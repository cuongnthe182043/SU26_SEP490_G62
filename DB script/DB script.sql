CREATE TABLE debts (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    debt_type       TEXT NOT NULL CHECK (debt_type IN ('customer','partner','driver')),
    customer_id     INT REFERENCES customers(id),
    partner_id      INT REFERENCES partners(id),
    driver_id       INT REFERENCES profiles(id),
    order_id        INT REFERENCES orders(id),
    shipment_id     INT REFERENCES order_shipments(id),   -- finer traceability
    total_amount    NUMERIC(12,2) NOT NULL CHECK (total_amount > 0),
    paid_amount     NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (paid_amount >= 0),
    due_date        DATE,
    status          TEXT NOT NULL DEFAULT 'unpaid'
                        CHECK (status IN ('unpaid','partial','paid','overdue')),
    notes           TEXT,
    updated_by      INT REFERENCES profiles(id),
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT debt_no_overpay CHECK (paid_amount <= total_amount)
);

