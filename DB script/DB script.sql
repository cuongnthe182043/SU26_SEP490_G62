CREATE TABLE kpi_records (
    id                      INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    driver_id               INT NOT NULL REFERENCES profiles(id),
    vehicle_group_id        INT NOT NULL REFERENCES vehicle_groups(id),
    month                   SMALLINT NOT NULL CHECK (month BETWEEN 1 AND 12),
    year                    SMALLINT NOT NULL CHECK (year >= 2020),
    completed_shipments     INT NOT NULL DEFAULT 0,
    total_revenue           NUMERIC(12,2) NOT NULL DEFAULT 0,  -- SUM(actual_price) only
    late_deliveries         INT NOT NULL DEFAULT 0,
    incident_count          INT NOT NULL DEFAULT 0,            -- all severities
    major_incident_count    INT NOT NULL DEFAULT 0,            -- severity = 'high'
    critical_incident_count INT NOT NULL DEFAULT 0,            -- severity = 'critical'
    on_time_rate            NUMERIC(5,2) GENERATED ALWAYS AS (
                                CASE WHEN completed_shipments = 0 THEN 0
                                ELSE ROUND(
                                    (completed_shipments - late_deliveries)::NUMERIC
                                    / completed_shipments * 100, 2)
                                END
                            ) STORED,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    UNIQUE (driver_id, month, year)
);


