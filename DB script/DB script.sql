-- =============================================================================
-- SECTION 16 — ACTIVITY LOGS (audit trail)
-- =============================================================================

CREATE TABLE activity_logs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT REFERENCES profiles(id),   -- NULL if system-generated
    action      TEXT NOT NULL,       -- e.g. 'shipment.status_changed', 'payroll.approved'
    entity_type TEXT NOT NULL,
    entity_id   BIGINT,
    old_data    JSONB,               -- snapshot before change
    new_data    JSONB,               -- snapshot after change
