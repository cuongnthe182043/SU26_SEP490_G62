CREATE TABLE expense_attachments (
    id          INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    expense_id  INT NOT NULL REFERENCES expenses(id) ON DELETE CASCADE,
    file_url    TEXT NOT NULL,
    uploaded_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 8 — MAINTENANCE RECORDS
-- =============================================================================

CREATE TABLE maintenance_records (
    id                  INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    vehicle_id          INT NOT NULL REFERENCES vehicles(id),
    expense_id          INT REFERENCES expenses(id),
    maintenance_type    TEXT NOT NULL CHECK (maintenance_type IN ('scheduled','repair','inspection','emergency')),
