    incident_type   TEXT NOT NULL
                        CHECK (incident_type IN (
                            'vehicle_breakdown',
                            'cargo_damage',
                            'road_incident',
                            'customer_refusal',   -- spec §23
                            'traffic_jam',        -- spec §23
                            'other'
                        )),
    severity_level  TEXT NOT NULL DEFAULT 'medium'
                        CHECK (severity_level IN ('low','medium','high','critical')),
    description     TEXT NOT NULL,
    location        TEXT,
    estimated_loss  NUMERIC(12,2) DEFAULT 0,
    status          TEXT NOT NULL DEFAULT 'open'
                        CHECK (status IN ('open','investigating','resolved','closed')),

    resolved_by             INT REFERENCES profiles(id),
    resolution_note         TEXT,
    replacement_vehicle_id  INT REFERENCES vehicles(id),
    replacement_driver_id   INT REFERENCES profiles(id),

