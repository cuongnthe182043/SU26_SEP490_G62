-- =============================================================================
-- SECTION 12 — PAYROLL
-- Base salary: <12 months → 8,000,000; >=12 months → 9,000,000
-- Revenue bonus: 15% revenue share
-- BHXH: 32% total (split company/employee — stored separately for transparency)
-- =============================================================================

CREATE TABLE payrolls (
    id                          INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    driver_id                   INT NOT NULL REFERENCES profiles(id),
    payroll_month               SMALLINT NOT NULL CHECK (payroll_month BETWEEN 1 AND 12),
    payroll_year                SMALLINT NOT NULL,

    -- Base
    base_salary                 NUMERIC(12,2) NOT NULL,   -- 8M or 9M depending on hire_date
    months_of_service           INT NOT NULL,              -- snapshot at generation time

    -- Revenue
    total_revenue               NUMERIC(12,2) NOT NULL DEFAULT 0,
    revenue_bonus               NUMERIC(12,2) NOT NULL DEFAULT 0,   -- 15% of total_revenue

    -- Bonuses (itemized for auditability)
    kpi_bonus                   NUMERIC(12,2) NOT NULL DEFAULT 0,
    top_driver_bonus            NUMERIC(12,2) NOT NULL DEFAULT 0,
    overtime_bonus              NUMERIC(12,2) NOT NULL DEFAULT 0,
    holiday_bonus               NUMERIC(12,2) NOT NULL DEFAULT 0,
    other_bonus                 NUMERIC(12,2) NOT NULL DEFAULT 0,

    -- Deductions (itemized)
    insurance_employee          NUMERIC(12,2) NOT NULL DEFAULT 0,   -- employee BHXH share
    insurance_company           NUMERIC(12,2) NOT NULL DEFAULT 0,   -- company BHXH share (cost center)
    advance_deduction           NUMERIC(12,2) NOT NULL DEFAULT 0,   -- salary advances repaid
    absence_penalty             NUMERIC(12,2) NOT NULL DEFAULT 0,
    other_deduction             NUMERIC(12,2) NOT NULL DEFAULT 0,

    -- Totals
    gross_salary                NUMERIC(12,2) GENERATED ALWAYS AS (
                                    base_salary
                                    + revenue_bonus
                                    + kpi_bonus + top_driver_bonus
                                    + overtime_bonus + holiday_bonus + other_bonus
                                ) STORED,
    net_salary                  NUMERIC(12,2) GENERATED ALWAYS AS (
                                    base_salary
                                    + revenue_bonus
                                    + kpi_bonus + top_driver_bonus
                                    + overtime_bonus + holiday_bonus + other_bonus
                                    - insurance_employee
                                    - advance_deduction
                                    - absence_penalty
                                    - other_deduction
                                ) STORED,

    -- Workflow: pending → reviewed → approved → paid
    status                      TEXT NOT NULL DEFAULT 'pending'
                                    CHECK (status IN ('pending','reviewed','approved','paid')),
    reviewed_by                 INT REFERENCES profiles(id),   -- Accountant
    reviewed_at                 TIMESTAMPTZ,
    approved_by                 INT REFERENCES profiles(id),   -- Manager
    approved_at                 TIMESTAMPTZ,
    paid_by                     INT REFERENCES profiles(id),
    paid_at                     TIMESTAMPTZ,

    UNIQUE (driver_id, payroll_month, payroll_year),
    created_at                  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at                  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 13 — SALARY ADVANCES
-- Workflow: Driver request → Accountant review → Manager approve/reject → Accountant pay
-- =============================================================================

CREATE TABLE salary_advances (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    driver_id       INT NOT NULL REFERENCES profiles(id),
    amount          NUMERIC(12,2) NOT NULL,
    reason          TEXT,
    request_month   SMALLINT NOT NULL CHECK (request_month BETWEEN 1 AND 12),
    request_year    SMALLINT NOT NULL,
    status          TEXT NOT NULL DEFAULT 'pending'
                        CHECK (status IN ('pending','approved','rejected','paid')),
    reviewed_by     INT REFERENCES profiles(id),   -- Accountant
    reviewed_at     TIMESTAMPTZ,
    approved_by     INT REFERENCES profiles(id),   -- Manager
    approved_at     TIMESTAMPTZ,
    paid_by         INT REFERENCES profiles(id),   -- Accountant
    paid_at         TIMESTAMPTZ,
    reject_reason   TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 14 — INCIDENTS
-- =============================================================================

CREATE TABLE incidents (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    shipment_id     INT NOT NULL REFERENCES order_shipments(id) ON DELETE CASCADE,
    reported_by     INT NOT NULL REFERENCES profiles(id),
    incident_type   TEXT NOT NULL
                        CHECK (incident_type IN ('vehicle_breakdown','cargo_damage','road_incident','other')),
    severity_level  TEXT NOT NULL DEFAULT 'medium'
                        CHECK (severity_level IN ('low','medium','high','critical')),
    description     TEXT NOT NULL,
    location        TEXT,
    estimated_loss  NUMERIC(12,2) DEFAULT 0,
    status          TEXT NOT NULL DEFAULT 'open'
                        CHECK (status IN ('open','investigating','resolved','closed')),
    occurred_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    resolved_at     TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Separate table for evidence images (replaces TEXT[] anti-pattern)
CREATE TABLE incident_evidences (
    id              INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    incident_id     INT NOT NULL REFERENCES incidents(id) ON DELETE CASCADE,
    file_url        TEXT NOT NULL,
    uploaded_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 15 — NOTIFICATIONS
