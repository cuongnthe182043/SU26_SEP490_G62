-- =============================================================================
-- SECTION 9 — DEBTS & DEBT PAYMENTS
-- =============================================================================

CREATE TABLE debts (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debt_type       TEXT NOT NULL CHECK (debt_type IN ('customer','partner','driver')),
    customer_id     BIGINT REFERENCES customers(id),
    partner_id      BIGINT REFERENCES partners(id),
    driver_id       BIGINT REFERENCES profiles(id),
