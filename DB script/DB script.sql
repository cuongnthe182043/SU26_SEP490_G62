CREATE VIEW v_leaderboard AS
SELECT
    k.vehicle_group_id,
    vg.name                     AS vehicle_group_name,
    k.year,
    k.month,
    k.driver_id,
    p.full_name                 AS driver_name,
    k.completed_shipments,
    k.total_revenue,
    k.on_time_rate,
    k.incident_count,
    RANK() OVER (
        PARTITION BY k.vehicle_group_id, k.year, k.month
        ORDER BY k.total_revenue DESC
    )                           AS revenue_rank,
    RANK() OVER (
        PARTITION BY k.vehicle_group_id, k.year, k.month
        ORDER BY k.completed_shipments DESC
    )                           AS trips_rank
FROM kpi_records k
JOIN profiles p       ON p.id  = k.driver_id
JOIN vehicle_groups vg ON vg.id = k.vehicle_group_id;

CREATE VIEW v_trip_pool AS
SELECT
    os.id               AS shipment_id,
    os.trip_code,
    os.order_id,
    os.vehicle_group_id,
    vg.name             AS vehicle_group_name,
    os.estimated_price,
    os.cargo_name,
    os.cargo_weight_kg,
    os.status,
    os.version,
    (SELECT address FROM trip_stops
     WHERE shipment_id = os.id AND stop_type = 'pickup'
     ORDER BY stop_index LIMIT 1)  AS first_pickup,
    (SELECT address FROM trip_stops
     WHERE shipment_id = os.id AND stop_type = 'delivery'
     ORDER BY stop_index DESC LIMIT 1) AS last_delivery,
    os.created_at
FROM order_shipments os
JOIN vehicle_groups vg ON vg.id = os.vehicle_group_id
WHERE os.status = 'available';

CREATE VIEW v_driver_debt_summary AS
SELECT
    d.driver_id,
    p.full_name                             AS driver_name,
    SUM(d.total_amount - d.paid_amount)     AS remaining_debt,
    COUNT(*) FILTER (WHERE d.status <> 'paid') AS open_debt_count
FROM debts d
JOIN profiles p ON p.id = d.driver_id
WHERE d.debt_type = 'driver'
  AND d.status IN ('unpaid','partial','overdue')
GROUP BY d.driver_id, p.full_name;

CREATE VIEW v_customer_debt_summary AS
SELECT
    c.id                                    AS customer_id,
    COALESCE(c.company_name, c.full_name)   AS customer_name,
    SUM(d.total_amount - d.paid_amount)     AS remaining_debt,
    COUNT(*) FILTER (WHERE d.status = 'overdue') AS overdue_count
FROM debts d
JOIN customers c ON c.id = d.customer_id
WHERE d.debt_type = 'customer'
  AND d.status IN ('unpaid','partial','overdue')
GROUP BY c.id, c.company_name, c.full_name;
