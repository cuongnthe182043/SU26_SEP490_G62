-- =============================================================================
-- SECTION 16 — ACTIVITY LOGS (audit trail)
-- =============================================================================

CREATE TABLE activity_logs (
    id          INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1 MINVALUE 100000 MAXVALUE 999999) PRIMARY KEY,
    user_id     INT REFERENCES profiles(id),   -- NULL if system-generated
