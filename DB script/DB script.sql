CREATE TABLE payrolls (
    id                      INT GENERATED BY DEFAULT AS IDENTITY (START WITH 100000 INCREMENT BY 1) PRIMARY KEY,
    driver_id               INT NOT NULL REFERENCES profiles(id),
    payroll_month           SMALLINT NOT NULL CHECK (payroll_month BETWEEN 1 AND 12),
    payroll_year            SMALLINT NOT NULL,

    base_salary             NUMERIC(12,2) NOT NULL,   -- 8M (<12 months) or 9M (≥12)
    months_of_service       INT NOT NULL,

    total_revenue           NUMERIC(12,2) NOT NULL DEFAULT 0,
    revenue_share_pct       NUMERIC(5,2) NOT NULL DEFAULT 15.00,
    revenue_bonus           NUMERIC(12,2) NOT NULL DEFAULT 0,

    kpi_bonus               NUMERIC(12,2) NOT NULL DEFAULT 0,
    top_driver_bonus        NUMERIC(12,2) NOT NULL DEFAULT 0,
    overtime_bonus          NUMERIC(12,2) NOT NULL DEFAULT 0,
    holiday_bonus           NUMERIC(12,2) NOT NULL DEFAULT 0,
    other_bonus             NUMERIC(12,2) NOT NULL DEFAULT 0,

    insurance_employee      NUMERIC(12,2) NOT NULL DEFAULT 0,
    insurance_company       NUMERIC(12,2) NOT NULL DEFAULT 0,  -- cost center only
    driver_debt_deduction   NUMERIC(12,2) NOT NULL DEFAULT 0,  -- spec §21
    advance_deduction       NUMERIC(12,2) NOT NULL DEFAULT 0,
    absence_penalty         NUMERIC(12,2) NOT NULL DEFAULT 0,
    other_deduction         NUMERIC(12,2) NOT NULL DEFAULT 0,

    gross_salary            NUMERIC(12,2) GENERATED ALWAYS AS (
                                base_salary + revenue_bonus
                                + kpi_bonus + top_driver_bonus
                                + overtime_bonus + holiday_bonus + other_bonus
                            ) STORED,

    -- spec §21: Net Salary = Gross − Driver Debt − deductions
    net_salary              NUMERIC(12,2) GENERATED ALWAYS AS (
                                base_salary + revenue_bonus
                                + kpi_bonus + top_driver_bonus
                                + overtime_bonus + holiday_bonus + other_bonus
                                - insurance_employee
                                - driver_debt_deduction
                                - advance_deduction
                                - absence_penalty
                                - other_deduction
                            ) STORED,

    status                  TEXT NOT NULL DEFAULT 'pending'
                                CHECK (status IN ('pending','reviewed','approved','paid')),
    reviewed_by             INT REFERENCES profiles(id),
    reviewed_at             TIMESTAMPTZ,
    approved_by             INT REFERENCES profiles(id),
    approved_at             TIMESTAMPTZ,
    paid_by                 INT REFERENCES profiles(id),
    paid_at                 TIMESTAMPTZ,

    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    UNIQUE (driver_id, payroll_month, payroll_year)
);


