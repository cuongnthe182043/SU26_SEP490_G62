

DROP SCHEMA IF EXISTS public CASCADE;
CREATE SCHEMA public;

CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- =============================================================================
-- SECTION 1 — AUTH & USER MANAGEMENT
-- =============================================================================

CREATE TABLE accounts (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email           TEXT UNIQUE NOT NULL,
    password_hash   TEXT NOT NULL,
    is_verified     BOOLEAN NOT NULL DEFAULT FALSE,
    last_login_at   TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Roles are fixed; seeded below
CREATE TABLE roles (
    id   SMALLINT PRIMARY KEY,
    name TEXT UNIQUE NOT NULL  -- manager | coordinator | accountant | driver
);

INSERT INTO roles (id, name) VALUES
    (1, 'manager'),
    (2, 'coordinator'),
    (3, 'accountant'),
    (4, 'driver');

CREATE TABLE profiles (
    id          BIGINT PRIMARY KEY REFERENCES accounts(id) ON DELETE CASCADE,
    full_name   TEXT NOT NULL,
    phone       TEXT UNIQUE,
    role_id     SMALLINT NOT NULL REFERENCES roles(id),
    avatar_url  TEXT,
    is_active   BOOLEAN NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 2 — VEHICLE & DRIVER
-- =============================================================================

CREATE TABLE vehicle_groups (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name                TEXT NOT NULL UNIQUE,           -- e.g. "Xe tải 1T", "Xe tải 2T"
    description         TEXT,
    max_load_weight_kg  NUMERIC(10,2),
    price_per_km        NUMERIC(12,2) NOT NULL,
    depreciation_per_km NUMERIC(10,2) NOT NULL DEFAULT 0,
    upgrade_allowed     BOOLEAN NOT NULL DEFAULT FALSE, -- allow driver to claim higher-group trip
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE vehicles (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plate_number        TEXT UNIQUE NOT NULL,
    vehicle_group_id    BIGINT NOT NULL REFERENCES vehicle_groups(id),
    brand               TEXT,
    model               TEXT,
    load_capacity_kg    NUMERIC(10,2),
    manufacture_year    SMALLINT,
    purchase_date       DATE,
    -- 1-1 with driver; set via trigger or application logic
    assigned_driver_id  BIGINT REFERENCES profiles(id),
    status              TEXT NOT NULL DEFAULT 'available'
                            CHECK (status IN ('available','in_delivery','maintenance','inactive')),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE drivers (
    -- profile_id IS the PK; driver = profile with role_id=4
    profile_id              BIGINT PRIMARY KEY REFERENCES profiles(id) ON DELETE CASCADE,
    vehicle_id              BIGINT UNIQUE REFERENCES vehicles(id),    -- 1-1 enforced by UNIQUE
    license_number          TEXT NOT NULL,
    license_expiry_date     DATE,
    hire_date               DATE NOT NULL,    -- used for base salary tier (<12 / >=12 months)
    emergency_contact_name  TEXT,
    emergency_contact_phone TEXT,
    revenue_share_percent   NUMERIC(5,2) NOT NULL DEFAULT 15.00,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 3 — CUSTOMERS & PARTNERS
-- =============================================================================

CREATE TABLE customers (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_type   TEXT NOT NULL CHECK (customer_type IN ('individual','business')),
    full_name       TEXT,
    company_name    TEXT,
    contact_person  TEXT,
    phone           TEXT NOT NULL,
    email           TEXT,
    address         TEXT,
    tax_code        TEXT,
    current_debt    NUMERIC(12,2) NOT NULL DEFAULT 0,
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE partners (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name    TEXT NOT NULL,
    contact_person  TEXT,
    phone           TEXT,
    email           TEXT,
    address         TEXT,
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 4 — ORDERS & SHIPMENTS
-- =============================================================================

CREATE TABLE orders (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id         BIGINT REFERENCES customers(id),
    created_by          BIGINT NOT NULL REFERENCES profiles(id),
    cargo_name          TEXT,
    cargo_weight_kg     NUMERIC(10,2),
    pickup_address      TEXT NOT NULL,
    delivery_address    TEXT NOT NULL,
    estimated_price     NUMERIC(12,2),
    actual_price        NUMERIC(12,2),
    payment_type        TEXT CHECK (payment_type IN ('cash','bank_transfer','client_credit')),
    status              TEXT NOT NULL DEFAULT 'pending'
                            CHECK (status IN ('pending','assigned','in_progress','completed','cancelled')),
    is_confidential     BOOLEAN NOT NULL DEFAULT FALSE,
    notes               TEXT,
    completed_at        TIMESTAMPTZ,
    updated_by          BIGINT REFERENCES profiles(id),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Each physical transport leg of an order
CREATE TABLE order_shipments (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id            BIGINT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    shipment_index      SMALLINT NOT NULL,           -- 1-based; used to detect "final trip"
    vehicle_group_id    BIGINT NOT NULL REFERENCES vehicle_groups(id),  -- pool filter key
    owner_driver_id     BIGINT REFERENCES profiles(id),  -- current active driver
    pickup_address      TEXT NOT NULL,
    delivery_address    TEXT NOT NULL,
    cargo_weight_kg     NUMERIC(10,2),
    estimated_price     NUMERIC(12,2),
    actual_price        NUMERIC(12,2),
    status              TEXT NOT NULL DEFAULT 'available'
                            CHECK (status IN (
                                'available','claimed','picking','loaded',
                                'transit','arrived','completed','cancelled','failed','returning'
                            )),
    -- Atomic claim guard: version incremented on every status change
    -- App does: UPDATE ... SET status='claimed', version=version+1 WHERE id=? AND version=?
    version             INT NOT NULL DEFAULT 0,
    notes               TEXT,
    -- Lifecycle timestamps (nullable until reached)
    claimed_at          TIMESTAMPTZ,
    picking_at          TIMESTAMPTZ,
    loaded_at           TIMESTAMPTZ,
    transit_at          TIMESTAMPTZ,
    arrived_at          TIMESTAMPTZ,
    completed_at        TIMESTAMPTZ,
    cancelled_at        TIMESTAMPTZ,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (order_id, shipment_index)
);

-- Who was assigned / self-claimed
CREATE TABLE shipment_assignments (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id         BIGINT NOT NULL REFERENCES order_shipments(id) ON DELETE CASCADE,
    driver_id           BIGINT NOT NULL REFERENCES profiles(id),
    vehicle_id          BIGINT NOT NULL REFERENCES vehicles(id),
    assignment_type     TEXT NOT NULL CHECK (assignment_type IN ('coordinator_assign','self_claim')),
    assigned_by         BIGINT REFERENCES profiles(id),  -- NULL when self_claim
    assigned_at         TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    accepted_at         TIMESTAMPTZ,
    completed_at        TIMESTAMPTZ
);

-- =============================================================================
-- SECTION 5 — PAYMENTS PER SHIPMENT
-- Requirements: multiple payments per trip, each may have multiple receipts
-- =============================================================================

CREATE TABLE shipment_payments (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id     BIGINT NOT NULL REFERENCES order_shipments(id) ON DELETE CASCADE,
    payment_type    TEXT NOT NULL CHECK (payment_type IN ('cash_collected','bank_transfer','client_credit')),
    amount          NUMERIC(12,2) NOT NULL,
    collected_by    BIGINT REFERENCES profiles(id),  -- driver who collected
    collected_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    notes           TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Receipt images per payment (cash requires ≥1; bank optional; credit none)
CREATE TABLE payment_receipts (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    payment_id      BIGINT NOT NULL REFERENCES shipment_payments(id) ON DELETE CASCADE,
    file_url        TEXT NOT NULL,
    uploaded_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 6 — COMPLETION PROOF (final trip of order only)
-- Must be real-time captured (no upload from device allowed — enforced at app layer)
-- =============================================================================

CREATE TABLE completion_proofs (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id        BIGINT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    shipment_id     BIGINT NOT NULL REFERENCES order_shipments(id),  -- must be final shipment
    captured_by     BIGINT NOT NULL REFERENCES profiles(id),
    -- is_realtime_capture enforced by app; stored here for audit
    is_realtime     BOOLEAN NOT NULL DEFAULT TRUE,
    file_url        TEXT NOT NULL,
    captured_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (order_id)   -- one proof per order
);

-- =============================================================================
-- SECTION 7 — EXPENSES
-- =============================================================================

CREATE TABLE expenses (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id     BIGINT REFERENCES order_shipments(id),
    vehicle_id      BIGINT REFERENCES vehicles(id),
    created_by      BIGINT NOT NULL REFERENCES profiles(id),
    expense_type    TEXT NOT NULL
                        CHECK (expense_type IN ('fuel','toll','parking','repair','maintenance','depreciation','other')),
    amount          NUMERIC(12,2) NOT NULL,
    description     TEXT,
    expense_date    DATE NOT NULL DEFAULT CURRENT_DATE,
    updated_by      BIGINT REFERENCES profiles(id),
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE expense_attachments (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    expense_id  BIGINT NOT NULL REFERENCES expenses(id) ON DELETE CASCADE,
    file_url    TEXT NOT NULL,
    uploaded_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =============================================================================
-- SECTION 8 — MAINTENANCE RECORDS
-- =============================================================================

CREATE TABLE maintenance_records (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id          BIGINT NOT NULL REFERENCES vehicles(id),
    expense_id          BIGINT REFERENCES expenses(id),
    maintenance_type    TEXT NOT NULL CHECK (maintenance_type IN ('scheduled','repair','inspection','emergency')),
