    paid_amount     NUMERIC(12,2) NOT NULL DEFAULT 0,
    due_date        DATE,
    status          TEXT NOT NULL DEFAULT 'unpaid'
                        CHECK (status IN ('unpaid','partial','paid','overdue')),
    notes           TEXT,
    updated_by      BIGINT REFERENCES profiles(id),
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE debt_payments (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debt_id         BIGINT NOT NULL REFERENCES debts(id) ON DELETE CASCADE,
    amount          NUMERIC(12,2) NOT NULL,
    payment_method  TEXT CHECK (payment_method IN ('cash','bank_transfer','offset')),
    paid_at         TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_by      BIGINT NOT NULL REFERENCES profiles(id),
    notes           TEXT
);

